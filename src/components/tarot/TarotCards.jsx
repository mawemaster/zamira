// Array com todas as cartas em ordem
export const ALL_CARDS = Object.values(TAROT_CARDS);

// Helper para pegar carta por ID
export const getCardById = (id) => TAROT_CARDS[id];

// Helper para pegar carta aleatória
export const getRandomCard = () => {
  const randomId = Math.floor(Math.random() * 22);
  return TAROT_CARDS[randomId];
};

// Helper para pegar múltiplas cartas aleatórias (sem repetir)
export const getRandomCards = (count = 3) => {
  const cards = [...ALL_CARDS];
  const selected = [];
  
  for (let i = 0; i < Math.min(count, cards.length); i++) {
    const randomIndex = Math.floor(Math.random() * cards.length);
    selected.push(cards[randomIndex]);
    cards.splice(randomIndex, 1);
  }
  
  return selected;
};
